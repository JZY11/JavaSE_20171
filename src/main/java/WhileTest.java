public class WhileTest {
    public static void main(String[] args) {
        int i = 0;
        while (i < 0){
            System.out.println(i);
            i++;
            System.out.println(i);
        }
        for (int j = 0; j < 10; j++){
            System.out.println(j);
        }
    }
}
