public class ConditionalOperators {
    public static void main(String[] args) {
        int x = 1;
        int y = 2;
        int z = (x != y) ? 1 : 100;  // !=  ==
//        boolean z =(x > y) ? true : false;
        System.out.println(z);
        //  三目运算符  (condition) ? (true value) : (false value)
    }
}
